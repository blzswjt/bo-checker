# 业务对象识别规则（用于判断某个事物是否是业务对象）
IDENTIFICATION_RULES = """
## 业务对象识别规则（共5条，需全部满足）

### 规则1：企业运作中不可缺少的重要人、事、物、地信息
- 可以基于商业设计、价值流、BI识别业务对象，并和业务能力匹配
- 针对业务对象，通常会建立相应流程、组织和IT进行管理
- 来源于业务流程的表、证、单、书，而非IT系统视角

**正例：** 在采购管理领域，采购需求作为采购流程的源头单据，承接前端业务需求（如工单、预算、项目），驱动货源确认、订单下达、到货验收等环节，为后续成本核算与财务结算提供依据，是非常重要的单据事项。

### 规则2：有唯一的身份标识信息
- 业务对象有唯一性身份标识信息，能区分业务对象的实例，且标识不变
- 业务对象的其他信息会慢变或者经常变化，但业务对象也不会发生本质变化

**正例：** 每一次采购需求单内容都不相同，因此采购需求有采购需求编号唯一标识。
**反例：** 将采购需求行作为业务对象是错误的，因为采购需求行没有独立的身份标识，采购需求行号可以基于每个采购需求编号下重新编码行号（不是全局唯一）。

### 规则3：相对独立并有一组实体描述
- 业务对象可独立存在，可获取、传输、使用，并发挥价值
- 逻辑数据实体依赖于业务对象
- 业务对象有一组描述自己某方面特征的逻辑实体；以主逻辑实体为核心，其他逻辑实体是他的外键子实体

**正例：** 采购需求与供应商、采购订单等业务对象，相对独立，由采购需求行、采购需求行等一组逻辑实体描述。
**反例：** 将采购需求行作为业务对象是错误的，因为采购需求行不能独立存在，其依赖于采购需求头。

### 规则4：有生命周期和状态变化
- 业务对象有生命周期，有状态变化
- 不同生命周期状态的人事物地，识别为不同的业务对象

**正例：** 采购需求有生命周期状态：草稿、审核中、已审批、已驳回、已退回、已作废。
**反例：** 基础数据（如果要变更，基本上是新加码值、清洗原有数据）；观测数据无状态变化（观测数据不会被修改）。

### 规则5：可实例化
- 业务对象的实例可以发生业务行为
- 业务对象有具体实例存在，通常需明确owner、定义架构、标准、度量监控，才能有效管理
- 业务对象的实例集合是不可提前预知、不限定数量的

**正例：** 采购需求有很多次需求，次数无法预置、不限；采购需求有相应的管理流程；采购需要有相应的行为：创建、审批、退回、取消等业务行为。
**反例：** 基础数据是分类/标签，无业务行为，比如采购需求类型；基础数据码值数量是提前确定、有限的；如果是报告报表数据，无法实例化。
"""

# 业务对象命名规则
NAMING_RULES = """
## 业务对象命名规则（共3条）

### 规则1：名称唯一
- 名称在数据模型中具有唯一性

**正例：** 业务对象"采购需求"，名称企业内唯一。
**反例：** 假设存在直接采购需求、间接采购需求、服务采购需求等业务对象，它们下面的逻辑实体包含的业务属性也不一样，则需要拆分为三个业务对象，且名称不能相同——异名异义。

### 规则2：名词命名
- 业务对象名称原则上必须是名词
- 原则上不使用介词/连词之类的虚词：在、被、的等均应去除
- 中文名称中尽量避免使用英文，禁止使用符号
- 开始和末尾原则上禁止使用数字

**正例：** 采购需求
**反例：** 采购需求申请

### 规则3：符合行规
- 名称能够完整准确表述业务含义，符合企业内、行业内的通用习惯和规范

**正例：** 采购需求
**反例：** 采购需求申请单
"""

# 用于 LLM 判断的完整 Prompt
CHECK_PROMPT = """你是一个数据治理专家，专门负责判断某个事物是否符合"业务对象"的定义。

## 业务对象识别规则（必须全部满足才能认定为业务对象）

1. **企业运作中不可缺少的重要人、事、物、地信息**：来源于业务流程的表、证、单、书，而非IT系统视角。通常会建立相应流程、组织和IT进行管理。

2. **有唯一的身份标识信息**：能区分业务对象的实例，且标识不变。其他信息可以变化，但业务对象不会发生本质变化。

3. **相对独立并有一组实体描述**：可独立存在、获取、传输、使用并发挥价值。有一组描述自己某方面特征的逻辑实体。

4. **有生命周期和状态变化**：有生命周期，有状态变化（如草稿、审核中、已审批等）。

5. **可实例化**：实例可以发生业务行为，有具体实例存在，实例集合不可提前预知、不限定数量。

## 常见不是业务对象的情况（反例）
- 基础数据/码值/分类/标签（如：采购需求类型、币种、国家代码）
- 业务对象的子实体/行项目（如：采购需求行、订单明细行）
- 观测数据/报告/报表数据
- 属性/字段（如：金额、日期、数量）
- 操作/动作/行为（如：审批、提交）
- 系统/模块/功能（如：采购系统、报表模块）

## 判断要求
请对给定的事物逐条规则分析，最后给出明确结论。

输出格式：
- **结论：** ✅ 是业务对象 / ❌ 不是业务对象 / ⚠️ 无法确定
- **理由：** 逐条规则分析
"""

